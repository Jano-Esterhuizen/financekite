export function PageLoader() {
  const [visible, setVisible] = useState(true);

  useEffect(() => {
    const timer = setTimeout(() => setVisible(false), 3000);
    return () => clearTimeout(timer);
  }, []);

  return (
    <AnimatePresence>
      {visible && (
        <motion.div
          className="fixed inset-0 z-[200] flex items-center justify-center bg-white px-8"
          exit={{ y: "-100%" }}
          transition={{ duration: 0.75, ease: [0.76, 0, 0.24, 1] }}
        >
          <svg
            viewBox="0 0 927 269"
            className="w-full max-w-[600px]"
            aria-label="FinanceKite"
          >
            <g transform="translate(0,269) scale(0.1,-0.1)">
              {/* Kite icon — rendered as filled silhouette so the full shape is visible */}
              <motion.path
                d={KITE_PATH}
                fill="hsl(190, 52%, 36%)"
                initial={{ opacity: 0 }}
                animate={{ opacity: 1 }}
                transition={{ duration: 0.6, ease: "easeOut", delay: 0 }}
              />

              {/* Letters — stroke draw animation */}
              {PATHS.map(({ d, delay }, i) => (
                <motion.path
                  key={i}
                  d={d}
                  fill="none"
                  stroke="#111827"
                  strokeWidth={18}
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  initial={{ pathLength: 0 }}
                  animate={{ pathLength: 1 }}
                  transition={{ duration: 0.85, delay, ease: "easeInOut" }}
                />
              ))}
            </g>
          </svg>
        </motion.div>
      )}
    </AnimatePresence>
  );
}
